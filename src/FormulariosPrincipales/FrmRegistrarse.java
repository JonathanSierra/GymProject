    public void registrarse()
    {
        try(Connection con = DriverManager.getConnection(conexion.getUrl(),conexion.getUsername(), conexion.getPassword()))
        {
    
            PreparedStatement pstm = con.prepareCall("call registrarUsuario(?,?)");
            pstm.setString(1, txtNombre.getText());
            pstm.setString(2,txtCedula.getText());
            pstm.executeQuery();
            JOptionPane.showMessageDialog(rootPane, "Usuario registrado correctamente");
            txtNombre.setText("");
            txtCedula.setText("");
            Statement stm = con.createStatement();
            String query ="call verUsuario()";
            ResultSet rs = stm.executeQuery(query);
            while(rs.next())
            {
                String usuario=rs.getString("usuario");
                String contrase単a=rs.getString("contrase単a");
                JOptionPane.showMessageDialog(rootPane, "Su usuario es: "+usuario+" su contrase単a es: "+contrase単a);
            }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }

